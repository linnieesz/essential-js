// operadores aritmeticos

// usando valores literais
console.log(10 + 5)
console.log(10 - 5)
console.log(10 * 5)
console.log(10 / 5)
console.log(10 % 5)

// usando variaveis
let x = 10;
let y = 5;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);

// incremento e decremento
let z = 10;

console.log(z++);
console.log(++z);
console.log(z);
console.log(z--);
console.log(--z);
console.log(z);
